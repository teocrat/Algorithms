a = int(input('Введите число a: '))
b = int(input('Введите число b: '))

print(f'Двоичное число {a}-и будет {bin(a)[2:]}')
print(f'Двоичное число {b}-и будет {bin(b)[2:]}')
print(f'Логическое ИЛИ = {bin(a | b)[2:]}.\nЛогическое И = {bin(a & b)[2:]}.\n'
      f'Логическое ИЛИ-НЕТ = {bin(a ^ b)[2:]}.')
print(f'Побитовый сдвиг вправо {a}-и на два знака = {bin(a >> 2)[2:]}')
print(f'Побитовый сдвиг влево {a}-и на два знака = {bin(a << 2)[2:]}')
